var songsLiked = ['Beat It', 'Hey Jude', 'Help', 'Piano Man'];

// push() appends to the end of a list
songsLiked.push('Scream');

// pop() deletes from the end of a list
songsLiked.pop();

// shift() deletes from the beginning of a list
songsLiked.shift();

// unshift() prepends to the beginning of a list
songsLiked.unshift('On the Road Again');

// Accessing array values
songsLiked[0] //will access the 0th value in the array
songsLiked[2] //will access the third value in the array