// = vs == vs ===

// '=' is assigning a value to a variable
// '===' is for strictly comparing values AND data types
	3 === '3'; //false

// '==' is for comparing values but not data types
	3 == '3'; //true


// Comparison operators (== and ===) can be used with any data type.
// They're not just for numbers and strings. For example, they can
// be used for objects.

{} === {};